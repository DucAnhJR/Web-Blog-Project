package entity;

import java.util.Date;

/**
 *
 * @author ADMIN
 */
public class Post {
    private int id;
    private String name; 
    private Date date;
    private String img, title, description;

    public Post() {
    }

    public Post(int id, String name, Date date, String img, String title, String description) {
        this.id = id;
        this.name = name;
        this.date = date;
        this.img = img;
        this.title = title;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "Post{" + "id=" + id + ", name=" + name + ", date=" + date + ", img=" + img + ", title=" + title + ", description=" + description + '}';
    }

}
